CREATE TABLE IF NOT EXISTS company (
    id_company INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name VARCHAR(255),
    company_tax_code VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS ai_subscription_tier (
    id_ai_subscription INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    label VARCHAR(255),
    description TEXT,
    token_input INT,
    token_output INT,
    price NUMERIC(12,2),
    unit VARCHAR(50),
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS sponsor_tier (
    id_sponsor_tier INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    priority_level INT,
    pricing_per_month NUMERIC(12,2),
    year_pack_sale NUMERIC(5,2),
    label VARCHAR(255),
    update_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS users (
    id_user INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    img_link TEXT,
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    role VARCHAR(50),
    hash_password VARCHAR(255),
    status VARCHAR(20),
    email VARCHAR(255) UNIQUE,
    phone VARCHAR(20),
    hash_tax_code VARCHAR(255),
    ai_tier INT,
    id_company INT,
    CONSTRAINT fk_users_ai_tier
        FOREIGN KEY (ai_tier) REFERENCES ai_subscription_tier(id_ai_subscription),
    CONSTRAINT fk_users_company
        FOREIGN KEY (id_company) REFERENCES company(id_company)
);

CREATE TABLE IF NOT EXISTS session (
    id_session INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_user INT,
    login_at TIMESTAMP,
    logout_at TIMESTAMP,
    login_date DATE,
    CONSTRAINT fk_session_user
        FOREIGN KEY (id_user) REFERENCES users(id_user)
);

CREATE TABLE IF NOT EXISTS transaction (
    id_transaction INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_subscription INT,
    id_sponsor INT,
    id_buyer INT,
    status VARCHAR(100),
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    invoice_date DATE,
    type VARCHAR(100),
    CONSTRAINT fk_transaction_subscription
        FOREIGN KEY (id_subscription) REFERENCES ai_subscription_tier(id_ai_subscription),
    CONSTRAINT fk_transaction_sponsor
        FOREIGN KEY (id_sponsor) REFERENCES sponsor_tier(id_sponsor_tier),
    CONSTRAINT fk_transaction_buyer
        FOREIGN KEY (id_buyer) REFERENCES users(id_user)
);

CREATE TABLE IF NOT EXISTS warehouse (
    id_warehouse INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_owner INT,
    name VARCHAR(255),
    address TEXT,
    description TEXT,
    location_address_text TEXT,
    location_province VARCHAR(255),
    location_commune VARCHAR(255),
    location_long NUMERIC(10,7),
    location_lat NUMERIC(10,7),
    location_postal_code VARCHAR(20),
    is_sponsor BOOLEAN DEFAULT FALSE,
    sponsor_type INT,
    status VARCHAR(50),
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_warehouse_owner
        FOREIGN KEY (id_owner) REFERENCES users(id_user),
    CONSTRAINT fk_warehouse_sponsor_type
        FOREIGN KEY (sponsor_type) REFERENCES sponsor_tier(id_sponsor_tier)
);

CREATE TABLE IF NOT EXISTS views (
    id_view INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_renter INT,
    id_warehouse INT,
    view_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_views_renter
        FOREIGN KEY (id_renter) REFERENCES users(id_user),
    CONSTRAINT fk_views_warehouse
        FOREIGN KEY (id_warehouse) REFERENCES warehouse(id_warehouse)
);

CREATE TABLE IF NOT EXISTS warehouse_image (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_warehouse INT NOT NULL,
    image_url TEXT NOT NULL,
    is_thumbnail BOOLEAN DEFAULT FALSE,
    display_order INT DEFAULT 0,
    CONSTRAINT fk_warehouse_image_warehouse
        FOREIGN KEY (id_warehouse) REFERENCES warehouse(id_warehouse)
);

CREATE TABLE IF NOT EXISTS warehouse_section (
    id_section INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_warehouse INT,
    label VARCHAR(255),
    sector INT,
    total_capacity NUMERIC(12,2),
    available_capacity NUMERIC(12,2),
    temp_min NUMERIC(5,2),
    temp_max NUMERIC(5,2),
    humidity NUMERIC(5,2),
    has_certification BOOLEAN DEFAULT FALSE,
    CONSTRAINT fk_warehouse_section_warehouse
        FOREIGN KEY (id_warehouse) REFERENCES warehouse(id_warehouse)
);

CREATE TABLE IF NOT EXISTS price_tier (
    id_price_tier INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_warehouse_section INT,
    label VARCHAR(255),
    value NUMERIC(12,2),
    unit VARCHAR(50),
    area_unit VARCHAR(50),
    CONSTRAINT fk_price_tier_section
        FOREIGN KEY (id_warehouse_section) REFERENCES warehouse_section(id_section)
);

CREATE TABLE IF NOT EXISTS certification_type (
    id_certification INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    label VARCHAR(255),
    update_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    law_references TEXT,
    pdf_link TEXT
);

CREATE TABLE IF NOT EXISTS certification_submit (
    id_cerf_submit INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    link TEXT,
    is_verified BOOLEAN DEFAULT FALSE,
    id_type INT,
    CONSTRAINT fk_certification_submit_type
        FOREIGN KEY (id_type) REFERENCES certification_type(id_certification)
);

CREATE TABLE IF NOT EXISTS warehouse_certification (
    id_warehouse INT NOT NULL,
    id_cerf_submit INT NOT NULL,
    PRIMARY KEY (id_warehouse, id_cerf_submit),
    CONSTRAINT fk_warehouse_certification_warehouse
        FOREIGN KEY (id_warehouse) REFERENCES warehouse(id_warehouse),
    CONSTRAINT fk_warehouse_certification_submit
        FOREIGN KEY (id_cerf_submit) REFERENCES certification_submit(id_cerf_submit)
);

CREATE TABLE IF NOT EXISTS rating (
    id_rating INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rate NUMERIC(2,1),
    comment TEXT,
    id_renter INT,
    warehouse_id INT,
    CONSTRAINT fk_rating_renter
        FOREIGN KEY (id_renter) REFERENCES users(id_user),
    CONSTRAINT fk_rating_warehouse
        FOREIGN KEY (warehouse_id) REFERENCES warehouse(id_warehouse)
);

CREATE TABLE IF NOT EXISTS bookmark (
    id_bookmark INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_user INT,
    id_warehouse INT,
    CONSTRAINT fk_bookmark_user
        FOREIGN KEY (id_user) REFERENCES users(id_user),
    CONSTRAINT fk_bookmark_warehouse
        FOREIGN KEY (id_warehouse) REFERENCES warehouse(id_warehouse)
);

CREATE TABLE IF NOT EXISTS ai_conversations (
    id_ai_conversations INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_user INT,
    criteria JSONB,
    message JSONB,
    total_input_tokens INT,
    total_output_tokens INT,
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ai_conversations_user
        FOREIGN KEY (id_user) REFERENCES users(id_user)
);

CREATE TABLE IF NOT EXISTS rent_request (
    id_rent_request INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_renter INT,
    id_warehouse INT,
    cargo_description TEXT,
    other_detail TEXT,
    duration INT,
    duration_unit VARCHAR(50),
    status VARCHAR(50),
    renter_rejection_reason TEXT,
    submit_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    start_date DATE,
    end_date DATE,
    offered_price NUMERIC(12,2),
    unit VARCHAR(50),
    owner_note TEXT,
    rejection_reason TEXT,
    CONSTRAINT fk_rent_request_renter
        FOREIGN KEY (id_renter) REFERENCES users(id_user),
    CONSTRAINT fk_rent_request_warehouse
        FOREIGN KEY (id_warehouse) REFERENCES warehouse(id_warehouse)
);

CREATE TABLE IF NOT EXISTS contract (
    id_contract INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_owner INT,
    id_renter INT,
    id_rent_request INT,
    cargo_description TEXT,
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(50),
    update_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    start_at DATE,
    end_at DATE,
    cancel_reason TEXT,
    payment_term TEXT,
    penalty_clause TEXT,
    special_term TEXT,
    owner_legal_name VARCHAR(255),
    owner_tax_code VARCHAR(255),
    owner_email VARCHAR(255),
    owner_phone VARCHAR(20),
    owner_address TEXT,
    renter_legal_name VARCHAR(255),
    renter_tax_code VARCHAR(255),
    renter_email VARCHAR(255),
    renter_phone VARCHAR(20),
    renter_address TEXT,
    CONSTRAINT fk_contract_owner
        FOREIGN KEY (id_owner) REFERENCES users(id_user),
    CONSTRAINT fk_contract_renter
        FOREIGN KEY (id_renter) REFERENCES users(id_user),
    CONSTRAINT fk_contract_rent_request
        FOREIGN KEY (id_rent_request) REFERENCES rent_request(id_rent_request)
);

CREATE TABLE IF NOT EXISTS rent_request_detail (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_rent_request INT NOT NULL,
    id_section INT NOT NULL,
    id_price_tier INT NOT NULL,
    rented_area NUMERIC(12,2),
    area_unit VARCHAR(50),
    CONSTRAINT fk_rent_request_detail_request
        FOREIGN KEY (id_rent_request) REFERENCES rent_request(id_rent_request),
    CONSTRAINT fk_rent_request_detail_section
        FOREIGN KEY (id_section) REFERENCES warehouse_section(id_section),
    CONSTRAINT fk_rent_request_detail_price_tier
        FOREIGN KEY (id_price_tier) REFERENCES price_tier(id_price_tier)
);

